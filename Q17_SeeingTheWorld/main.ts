const places = ["Istanbul", "Moscow", "Tokyo", "New York City", "Medina"];
// Original array
console.log(places)
console.log("\n\n")

// Copy of array and then sorted
console.log(places.slice(0).sort())
console.log("\n\n")

// Original array is still as same
console.log(places)
console.log("\n\n")

// Reversing the order of list
console.log(places.reverse())
console.log("\n\n")

// Reversing again the order back to original order
console.log(places.reverse())
console.log("\n\n")

// Sorting array
console.log(places.sort())
console.log("\n\n")

// Reversing sorted array
console.log(places.reverse())
console.log("\n\n")