const favouriteFruits = ["Mango", "Banana", "Orange"];
for(let i = 0; i < favouriteFruits.length; i++) {
    if(favouriteFruits[i] == "Banana"){
        console.log("You really like Bananas!");
    } else if(favouriteFruits[i] == "Kiwi") {
        console.log("Found your favourite KIWI")
    } else if(favouriteFruits[i] == "Mango") {
        console.log("Yummy! Mangoes on the way!!")
    } else if(favouriteFruits[i] == "Grapes") {
        console.log("Grapes found!!!")
    } else if(favouriteFruits[i] == "Orange") {
        console.log("Juicy Oranges are ready to eat!")
    }
}